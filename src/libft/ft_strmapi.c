#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		size;

	if (!s || !f)
		return (NULL);
	size = ft_strlen((char *)s);
	if ((str = (char*)malloc(sizeof(char) * (size + 1))))
	{
		i = 0;
		while (s[i])
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
